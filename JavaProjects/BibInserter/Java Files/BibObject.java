package BibInserter;

public class BibObject {

	public String BibKey;
	public String Author ;
	public String Title ;
	public String Year ;

	/*
	 * Construcor
	 */
	public BibObject(String bibKey, String author, String title, String year) {

		this.BibKey = bibKey;
		this.Author = author;
		this.Title = title;
		this.Year = year;
	}





}
